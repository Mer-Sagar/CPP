//1. Simple Number Triangle Pattern

#include <iostream>  
using namespace std;  

int main()  
{  
	int i,j,k,l,n;    
	cout<<"Enter the Range=";    
	cin>>n;    
	
	for(i=1;i<=n;i++)    
	{    
		for(k=1;k<=i;k++)    
		{    
			cout<<i;    
		}    
	   
	cout<<"\n";    
	}  
	
return 0;  
}  